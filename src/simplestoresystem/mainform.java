    private void editOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editOrderActionPerformed
        int selectedRow = orderTable.getSelectedRow();
        int col =  2;
        int initialQuantity = Integer.parseInt(orderTable.getValueAt(selectedRow, 1).toString());
        double initialPrice = Double.parseDouble(orderTable.getValueAt(selectedRow, col).toString());
        double originalPrice = initialPrice/initialQuantity;
        if(selectedRow>-1){
            String initQuantity = JOptionPane.showInputDialog(this, "Enter new quantity: ",order.quantity.get(selectedRow));
            int quantity = Integer.parseInt(initQuantity);
            double price = quantity * originalPrice;
            order.editQuantity(selectedRow, quantity, price);
            refreshOrder();
            priceLabel.setText(Double.toString(order.getTotalPrice()));
        }
    }//GEN-LAST:event_editOrderActionPerformed

