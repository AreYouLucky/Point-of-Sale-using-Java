    private void searchProductKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchProductKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            
            String number = JOptionPane.showInputDialog("Enter quantity: ", 1);
            int quantity = Integer.parseInt(number);
            int colID = 0,colName = 1, colPrice = 2, row=0;
            int recordId = Integer.parseInt(recordTable.getValueAt(row, colID).toString());
            String recordName = recordTable.getValueAt(row, colName).toString();
            double initialPrice = Double.parseDouble(recordTable.getValueAt(row, colPrice).toString());
            double price = initialPrice * quantity;
            order.addRecord(recordId, recordName, quantity, price);
            refreshOrder();
            priceLabel.setText(Double.toString(order.getTotalPrice()));
      }
    }//GEN-LAST:event_searchProductKeyPressed

