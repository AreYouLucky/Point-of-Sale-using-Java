    private void addProduct3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addProduct3ActionPerformed
        String money = JOptionPane.showInputDialog(this, "Enter cash amount: ", "Calculate Change", JOptionPane.INFORMATION_MESSAGE);
        double change = Double.parseDouble(money) - order.getTotalPrice();
        JOptionPane.showMessageDialog(this, "Change amount = "+change, "Change result", JOptionPane.INFORMATION_MESSAGE);
        if(Double.parseDouble(money)>0){
            order.saveOrder();
            order.clearOrder();
            refreshOrder();
            refreshRecord();
            refreshProduct();
            refreshSale();
            priceLabel.setText(Double.toString(order.getTotalPrice()));
        }
    }//GEN-LAST:event_addProduct3ActionPerformed

